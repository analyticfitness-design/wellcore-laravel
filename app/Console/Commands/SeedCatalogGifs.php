    public function handle(): int
    {
        $catalog = $this->getCatalog();
        $rows = [];
        $now = now();

        foreach ($catalog as $nombre => $filename) {
            $gif_url = self::CDN.$filename;
            $slug = \Illuminate\Support\Str::slug($nombre);
            $rows[] = [
                'slug'         => $slug,
                'nombre'       => $nombre,
                'gif_url'      => $gif_url,
                'descargado'   => false,
                'created_at'   => $now,
                'updated_at'   => $now,
            ];
        }

        // Upsert in chunks to avoid query size limits
        $chunks = array_chunk($rows, 100);
        $total = 0;
        foreach ($chunks as $chunk) {
            DB::table('ejercicios_fitcron')->upsert(
                $chunk,
                ['slug'],
                ['nombre', 'gif_url', 'updated_at']
            );
            $total += count($chunk);
        }

        $this->info("Sembrados/actualizados {$total} ejercicios en ejercicios_fitcron.");
        $this->info('Cache de ExerciseMediaService limpiada (exercise_gif_map_v2).');
        \Illuminate\Support\Facades\Cache::forget('exercise_gif_map_v2');

        return self::SUCCESS;
    }
